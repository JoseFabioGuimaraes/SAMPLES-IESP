public class usuario {
    String nome_do_usuario;
    String email_do_usuario;
    int senha;
    String lista_de_favoritos;

    void nome_do_usuario(String nome_do_usuario){
        nome_do_usuario = nome_do_usuario;
    }
    void email_do_usuario(String email_do_usuario){
        email_do_usuario = email_do_usuario;
    }
    void senha(int senha){
        senha = senha;
    }
    void lista_de_favoritos(String lista_de_favoritos){
        lista_de_favoritos = lista_de_favoritos;
    }

}
